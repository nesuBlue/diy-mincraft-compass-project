int lastFrameIndex = -1;

// ─── Draw a Piskel frame ──────────────────────────────────────────────────────
// Piskel stores pixels as 0xAABBGGRR, so:
//   R = color & 0xFF
//   G = (color >> 8)  & 0xFF
//   B = (color >> 16) & 0xFF
//   A = (color >> 24) & 0xFF  ← 0x00 means transparent/off
void drawPattern(int frameIndex) {
  if (frameIndex == lastFrameIndex) return;
  lastFrameIndex = frameIndex;

  matrix->clear();

  for (int i = 0; i < 64; i++) {
    uint32_t pixel = pgm_read_dword(&minecraft_compass_data[frameIndex][i]);

    // In RGBA (0xRRGGBBAA), Alpha is the last byte. 
    // We check if Alpha > 0 so we don't draw "transparent" pixels.
    if ((pixel & 0xFF) > 0) {
      uint8_t r = (pixel >> 24) & 0xFF; // Red is the first byte
      uint8_t g = (pixel >> 16) & 0xFF; // Green is the second byte
      uint8_t b = (pixel >> 8) & 0xFF;  // Blue is the third byte

      int col = i % 8;
      int row = i / 8;
      matrix->drawPixel(col, row, matrix->Color(r, g, b));
    }
  }
  matrix->show();
}

